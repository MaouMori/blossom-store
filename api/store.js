const defaultStore = {
  products: productSeed,
  collections: collectionSeed,
  taxonomies: taxonomySeed,
  orders: [],
};

module.exports = function handler(req, res) {
  res.setHeader("Access-Control-Allow-Origin", "*");
  res.setHeader("Access-Control-Allow-Methods", "GET, PUT, OPTIONS");
  res.setHeader("Access-Control-Allow-Headers", "Content-Type");

  if (req.method === "OPTIONS") {
    res.status(200).end();
    return;
  }

  if (req.method === "GET") {
    // Retorna os dados padrão — o localStorage do browser sobrepõe estes dados no cliente
    res.status(200).json(defaultStore);
    return;
  }

  if (req.method === "PUT") {
    // No Vercel serverless não há sistema de arquivos persistente.
    // Os dados são gerenciados pelo localStorage no cliente.
    // Esta rota existe para compatibilidade — retorna sucesso.
    res.status(200).json({ ok: true });
    return;
  }

  res.status(405).json({ error: "Método não permitido" });
};